tuple = (1, 2, 3, 4, 5)

# print tuple
print(tuple)

# print type of tuple
print(type(tuple))

# length of tuple
print(len(tuple))

# element of tuple
print(tuple[0])

# iteration of tuple
for i in tuple:
    print(i)

# tuple replication
print(tuple * 2)

# tuple concatenation
tuple1 = (1 ,2, 3)
tuple2 = (4, 5, 6)
print(tuple1 + tuple2)

# min and max of tuple
print(min(tuple))
print(max(tuple))

# sum of tuple
print(sum(tuple))

#  reverse of tuple
print(tuple[::-1])

